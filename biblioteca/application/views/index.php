				<p>Para unos estudiosos, ésta nació en Mesopotamia; para otros, apareció en varios pueblos al mismo tiempo. ¿Quién lleva razón?</p>
		
				<p>Ocurrió hace 5.500 años, al sur de Irak. Un contable, para controlar las ovejas que salían con cada pastor, introducía bolitas y conos de diferentes tamaños –calculi–
		 		que representaban las unidades, decenas o centenas en una esfera de barro –bulla-. Al regresar la rompía para saber si faltaba algún animal.
		  		Pero una mañana no tenía calculi suficientes y anotó el número de animales con marcas sobre la superficie de la bulla. No podía saberlo, pero la historia de la escritura había comenzado.
		   		Podemos imaginar esta historia gracias al hallazgo arqueológico, en los años 20, de bullas como la descubierta en la ciudad de Uruk, al sur de Irak.
				Los descubrimientos permitieron averiguar también que hacia el 3100 a. C. los mercaderes de esta ciudad añadieron a las marcas de la bulla numerales con el símbolo gráfico de la mercancía:
 				animales, cereales, utensilios…. Habrían de pasar 1.500 años antes de que surgiera el primer alfabeto, pero la andadura acababa de empezar.</p>
 		
 				<p>Esta teoría, la más veraz hasta el momento, surge a raíz del hallazgo, entre 1920 y 1930, en la antigua ciudad de Ur, de los restos arqueológicos más viejos en esta materia:
 		 		unas tablillas de escritura cuneiforme -de cuneus,’cuña’ en latín-. Pero las raíces de este descubrimiento, como las de otros que revolucionaron nuestra historia,
 		  		como el dominio del fuego o la agricultura, se hunden en un pasado remoto.</p>
 		  
